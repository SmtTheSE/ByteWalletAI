#  Phase 1: Chat models  #

class ChatRequest(BaseModel):
    """
    Natural-language financial question with a full wallet snapshot for context.
    The snapshot mirrors PredictBurnRateRequest so the LLM tools can access live data.
    """
    user_id: str
    question: str = Field(..., description="Free-form natural-language question, e.g. 'Can I afford a trip?'")
    # Wallet context — mirrors PredictBurnRateRequest fields
    currency: str = "VND"
    current_date: str = Field(..., description="ISO date string, e.g. '2026-03-15'")
    balances: Balances
    monthly_budget: MonthlyBudget
    essential_obligations: List[EssentialObligation] = []
    transactions: List[Transaction] = Field(default=[], description="Current month transactions")
    nickname: Optional[str] = None


class ChatResponse(BaseModel):
    user_id: str
    question: str
    answer: str                            # Plain-language LLM-generated answer
    tools_used: List[str] = []            # Which wallet tools the LLM invoked
    fallback_mode: bool = False           # True if Gemini was unavailable
    generated_at: datetime
